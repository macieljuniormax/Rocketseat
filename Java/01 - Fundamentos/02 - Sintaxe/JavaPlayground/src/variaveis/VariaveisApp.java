package variaveis;

public class VariaveisApp {
  public static void main(String[] args) {
    final String BR_PAIS = "Brasil"; // cria uma constante imutável
    double PI = 3.14;

    System.out.println(BR_PAIS);
    System.out.println(PI);

    //  As variáveis:
    //    • Deve conter apenas letras, - (underline), $ ou os números de e a 9 ;
    //    • Deve obrigatoriamente se iniciar por uma letra (preferencialmente), $, jamais com número;
    //    • Deve iniciar com uma letra minúscula (boa prática - ver abaixo);
    //    • Não pode conter espaços;
    //    • Não podemos usar palavras-chave da linguagem;
    //    • O nome deve ser único dentro de um escopo.
  }
}
