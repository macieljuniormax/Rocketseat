package Operadores;

public class Relacionais {
    public static void main(String[] args) {
        int numero1 = 1;
        int numero2 = 2;

        if (numero1 > numero2) {
            System.out.println("Número 1 é maior que o número 2");
        }

        if (numero1 < numero2) {
            System.out.println("Número 1 é menor que o número 2");
        }

        if (numero1 >= numero2) {
            System.out.println("Número 1 é maior ou igual ao número 2");
        }

        if (numero1 <= numero2) {
            System.out.println("Número 1 é menor ou igual ao número 2");
        }

        if (numero1 != numero2) {
            System.out.println("Número 1 é diferente do número 2");
        }
    }
}
